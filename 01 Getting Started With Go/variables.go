package main

import "fmt"

func main() {

	// A variable of type integer is created and assigned to a value
	var i int = 10
	fmt.Println("The value of i is", i)

	// Here the compliler automatically infers the type from the value
	var j = 5

	fmt.Println("The value of j is ", j)

	// A shorthand syntax for creating new variables.
	k := -1
	fmt.Println("The value of k is ", k)

}
